				A ideia do projeto surge devido a um intenso crescimento populacional e geográfico da cidade de Janaúba, Minas Gerais. Segundo dados do Instituto Brasileiro de Geografia e Estatística a população aumentou em aproximadamente 10.000 habitantes entre 2010 e 2018. Dessa forma, observou-se a necessidade de algo que abrigasse todas informações sobre os contatos comerciais da cidade, desde lanchonetes e restaurantes a serviços de manutenção como oficinas entre outros departamentos. As pesquisas feitas mostram que não existe necessariamente um local único onde todas as informações se encontram compactadas e resumidas. 
				A partir de tal necessidade, foi desenvolvido o projeto “My home Janaúba”, website que abriga tais informações de forma simples, organizada e segura, proporcionando ao usuário mobilidade e rapidez em sua pesquisa. O site será desenvolvido utilizando todo o conhecimento acumulado nos três anos de curso integrado, aplicando-os de forma objetiva e transparente a fim de demonstrar como os conhecimentos agregaram na vida acadêmica e poderão contribuir futuramente na vida profissional. Dessa forma o website vai agregar bastante para a sociedade gorutubana e facilitar o acesso dos turistas aos pontos comerciais da cidade. 

